package com.mycompany.atividades_praticas_1.aula1;

public class DiferencaPrintPrintln {
    public static void main(String[] args) {
        System.out.println("Demonstracao da diferenca entre print e println:");
        
        System.out.println("--- Usando println ---");
        System.out.println("A palavra 'println' imprime o texto e salta para a proxima linha.");
        System.out.println("Esta frase esta em uma nova linha.");
        
        System.out.println("\n--- Usando print ---");
        System.out.print("A palavra 'print' imprime o texto ");
        System.out.print("e continua na mesma linha. ");
        System.out.print("Veja que esta frase continua ao lado.");
        System.out.println();
    }
}
