public class Funcionario {
    private String nome;
    private double salario;

    // Construtor da classe pai
    public Funcionario(String nome, double salario) {
        this.nome = nome;
        this.salario = salario;
    }

    public String getNome() {
        return nome;
    }

    public double getSalario() {
        return salario;
    }
}

